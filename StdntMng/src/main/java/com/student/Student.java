package com.student;

public class Student {
	
	String studentId;
	String stduentname;
	String stduentAddress;
	String stduentDOB;
	String studentGender;
	String stduentcourse;
	String studentPassword;
	public String getStudentId() {
		return studentId;
	}
	public void setStudentId(String studentId) {
		this.studentId = studentId;
	}
	public String getStduentname() {
		return stduentname;
	}
	public void setStduentname(String stduentname) {
		this.stduentname = stduentname;
	}
	public String getStduentAddress() {
		return stduentAddress;
	}
	public void setStduentAddress(String stduentAddress) {
		this.stduentAddress = stduentAddress;
	}
	public String getStduentDOB() {
		return stduentDOB;
	}
	public void setStduentDOB(String stduentDOB) {
		this.stduentDOB = stduentDOB;
	}
	public String getStudentGender() {
		return studentGender;
	}
	public void setStudentGender(String studentGender) {
		this.studentGender = studentGender;
	}
	public String getStduentcourse() {
		return stduentcourse;
	}
	public void setStduentcourse(String stduentcourse) {
		this.stduentcourse = stduentcourse;
	}
	public String getStudentPassword() {
		return studentPassword;
	}
	public void setStudentPassword(String studentPassword) {
		this.studentPassword = studentPassword;
	}
	

}
